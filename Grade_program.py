"""
برنامج تقدير درجات الطلاب
يقوم بقراءة درجة الطالب (0-100) وإخراج التقدير المناسب:
- ضعيف: أقل من 50
- جيد: 50 إلى أقل من 65
- جيد جداً: 65 إلى أقل من 80
- ممتاز: 80 إلى 100

يستمر البرنامج في العمل حتى يكتب المستخدم كلمة "خروج"
"""

def evaluate_grade(score: int) -> str:
    """
    إرجاع التقدير المناسب بناءً على الدرجة.
    
    Args:
        score: درجة الطالب (عدد صحيح بين 0 و100)
    
    Returns:
        التقدير النصي (ضعيف، جيد، جيد جداً، ممتاز)
    """
    if score < 50:
        return "ضعيف"
    elif score < 65:      # 50-64
        return "جيد"
    elif score < 80:      # 65-79
        return "جيد جداً"
    else:                 # 80-100
        return "ممتاز"

def main():
    """الدالة الرئيسية لتشغيل البرنامج مع حلقة التكرار ومعالجة الأخطاء."""
    print("=" * 50)
    print("برنامج تقدير درجات الطلاب".center(50))
    print("=" * 50)
    print("تعليمات: أدخل درجة بين 0 و100، أو اكتب 'خروج' لإنهاء البرنامج.\n")
    
    while True:
        # طلب إدخال من المستخدم
        user_input = input(">>> أدخل درجة الطالب (أو 'خروج'): ").strip()
        
        # التحقق من رغبة المستخدم في الخروج (بغض النظر عن حالة الأحرف)
        if user_input.lower() == "خروج":
            print("\nتم إنهاء البرنامج. شكراً لاستخدامك!")
            break
        
        # محاولة تحويل الإدخال إلى رقم صحيح
        try:
            score = int(user_input)
        except ValueError:
            # حالة إدخال نص غير "خروج" أو رقم
            print("خطأ: الرجاء إدخال رقم صحيح بين 0 و100 فقط.\n")
            continue
        
        # التحقق من صحة النطاق
        if score < 0 or score > 100:
            print("خطأ: الدرجة يجب أن تكون بين 0 و100.\n")
            continue
        
        # حساب التقدير وعرضه
        grade = evaluate_grade(score)
        print(f"تقدير الطالب (درجته: {score}) هو: [{grade}]\n")

# التأكد من تشغيل البرنامج عند استدعاء الملف مباشرة
if __name__ == "__main__":
    main()
